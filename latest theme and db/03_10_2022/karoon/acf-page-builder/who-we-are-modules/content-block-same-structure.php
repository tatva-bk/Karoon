      <h2>Environmental Monitoring</h2>
      <p>Environmental monitoring is carried out in four main areas:</p>
      <ul>
        <li>Water and Plankton Monitoring – through analysis of physical-chemical and biological data collected from water and plankton samples taken at various points around the FPSO</li>
        <li>Sediment and Benthic Monitoring – through analysis of physical-chemical and biological data collected from marine sediment samples</li>
        <li>Produced Water Monitoring – on a daily basis of oil and grease content in discharged water and an additional twice yearly special sample collection</li>
        <li>Spill Monitoring – through monitoring and analysis of metocean data.</li>
      </ul>
      <p>
        Karoon has consistently returned results within allowable regulatory parameters, generally significantly outperforming
        the environmental requirements of our licence. Details of Karoon’s environmental performance statistics can be
        found in our annual Sustainability Repor
      </p>

      <h2>Water Management</h2>
      <p>With water scarcity affecting more than 40% of the world’s population, Karoon recognises that sustainable water
            management is an important contributor to climate resilience. Fresh water is used in Karoon offices and shore bases
            through the public water supply. The only fresh water used in offshore operations is that required for consumption
            by employees and service providers and is transported from shore. Freshwater is only used for operational purposes
            on supply vessels, which is obtained from the shorebase water supply.
        </p>

      <h2>Waste Management Plan</h2>
      <p>The Baúna waste management plan, also called the Pollution Control Plan, ensures waste is minimised wherever possible and that any residual waste is properly treated and disposed of through:</p>
      <ul>
        <li>Minimising the generation of waste, wastewater and emissions in production activities</li>
        <li>Recycling as much of the waste returned onshore as possible</li>
        <li>Ensuring the final disposal of residual waste is in accordance with current standards and regulations </li>
        <li>Seeking ongoing improvements to minimise pollution from air emissions, solid waste and wastewater discharge at sea.</li>
    </ul>
    <h2>Environmental Protection</h2>
    <p>In addition to our regulatory licence projects, Karoon has sought opportunities to undertake projects aimed at protecting the local environment. One such project is the Sun Coral Project, in which Karoon is providing funding for research of sun coral and how local environments can be protected from sun coral invasion.</p>

    <h2>Environmental Contributions</h2>
    <p>As an oil producer in Brazil, Karoon provides significant environmental contributions (environmental compensation payments) to protect state and federal parks in Brazil. The payments for Baúna total more than US$1.8 million and contribute to the upkeep and protection of:</p>
