        <h2>Why Brazil?</h2>
        <p>Karoon entered Brazil in 2007, following the award of five exploration blocks in the offshore Santos Basin. Currently,
            Karoon has a 100% working interests in four blocks, which contain the producing Baúna field and undeveloped Patola
            field, the Neon and Goiá oil discoveries, and the Clorita exploration prospect
        </p>
        <p>
        Brazil is an attractive place to invest in the oil and gas industry due to the following:
        </p>
        <ul>
            <li>High prospectivity for oil and gas in offshore conventional reservoirs</li>
            <li>Well-established oil and gas industry, with a stable regulatory regime.</li>
            <li>Favourable tax-royalty contract terms for post-salt licences</li>
            <li>Well-developed drilling and services sector</li>
            <li>World class professional labour force.</li>
            <li>Active asset trade market.</li>
        </ul>
        <p>Previously dominated by State-owned Petrobras, the Brazilian upstream sector was opened up to foreign investment in
        1998, through the first Bid Round. Brazil now has a diverse set of international companies, including BP, Shell, Chevron
        and Equinor, active in all aspects of exploration and production, as well as numerous smaller independent companies,
        such as Karoon. The internationalisation of the Brazilian oil and gas industry has brought access to global financial
        markets, with the sector well served by debt and equity markets, providing the capital required to fund operations and
        support growth projects.
        </p>
        <p>In 2021, Brazil was the world’s 10th largest oil producer, averaging 3.5 million barrels of oil per day, or approximately
        3.5% of global supply. Brazil is targeting to become the world’s fifth largest oil producer by 2030, with more than US$20
        billion per annum expected to be invested into exploration and production by Petrobras, major companies and the
        independents. Driving this growth is the development of a number of giant discoveries made in recent years in the presalt basins. In addition, there remains significant untapped potential due to a large number of prospective sedimentary
        basins which are only lightly explored
        </p>
        <p>The service sector backing the industry is well developed. The world’s leading drilling contractors and well servicing
        and evaluation companies are active and competitive in Brazil, with established in-country operations. For oil and gas
        companies, there is a deep pool of highly trained geoscientists and engineers.
        </p>
        <p>While most of the major oil companies are focused on finding and developing large pre-salt fields, some older and
        smaller assets are being divested by these companies. Karoon believes that given its current size, production base, core
        skills and access to capital, it is well positioned to participate in future asset transaction processes, either alone or in
        partnership with like-minded companies
        </p>
        <p>The majority of Brazil’s oil production is from the Santos and Campos basins. Karoon’s operations are located in the western part of the Santos Basin
        </p>
